package org.qishi.micromark.pojo;

public class OrderQuery {
	private String appid;
	private String timestamp;
	private String app_signature;
	private String sign_method;
	private String package_bak;
	public String getAppid() {
		return appid;
	}
	public void setAppid(String appid) {
		this.appid = appid;
	}
	public String getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}
	public String getApp_signature() {
		return app_signature;
	}
	public void setApp_signature(String app_signature) {
		this.app_signature = app_signature;
	}
	public String getSign_method() {
		return sign_method;
	}
	public void setSign_method(String sign_method) {
		this.sign_method = sign_method;
	}
	public String getPackage_bak() {
		return package_bak;
	}
	public void setPackage_bak(String package_bak) {
		this.package_bak = package_bak;
	}
	
	
	
	

}
